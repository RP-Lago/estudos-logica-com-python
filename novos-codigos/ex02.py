# Criando um algoritmo em python que use variaveis e listas

# Definindo variáveis
nome = "João"
idade = 25

# Definindo uma lista de hobbies
hobbies = ["ler", "escrever", "correr"]

# Exibindo informações
print(f"Nome: {nome}")
print(f"Idade: {idade}")
print("Hobbies:")

# Iterando sobre a lista de hobbies e exibindo cada um
for hobby in hobbies:
    print(f"- {hobby}")